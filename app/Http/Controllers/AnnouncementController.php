    { 
        $data = $this->validate($request, [
            'user_id'       => 'nullable',
        	'title'  	    => 'required',
	        'announcement'	=> 'nullable', 
            'image' 		=> 'nullable'

        ]);
        $data['user_id'] = Auth::user()->id;
       

        // dd($data);

        Announcement::create($data);
        
        return redirect()->back();
    }

